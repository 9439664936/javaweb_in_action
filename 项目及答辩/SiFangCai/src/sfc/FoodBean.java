package sfc;

public class FoodBean {
	private String foodName;
	private double foodPrice;
	private double foodVip;
	private String foodType;
	private String foodIngredient;
	private int foodQuantity;
	public String getFoodName() {
		return foodName;
	}
	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}
	public double getFoodPrice() {
		return foodPrice;
	}
	public void setFoodPrice(double foodPrice) {
		this.foodPrice = foodPrice;
	}
	public double getFoodVip() {
		return foodVip;
	}
	public void setFoodVip(double foodVip) {
		this.foodVip = foodVip;
	}
	public String getFoodType() {
		return foodType;
	}
	public void setFoodType(String foodType) {
		this.foodType = foodType;
	}
	public String getFoodIngredient() {
		return foodIngredient;
	}
	public void setFoodIngredient(String foodIngredient) {
		this.foodIngredient = foodIngredient;
	}
	public int getFoodQuantity() {
		return foodQuantity;
	}
	public void setFoodQuantity(int foodQuantity) {
		this.foodQuantity = foodQuantity;
	}
	
	
}
